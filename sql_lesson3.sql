
IF OBJECT_ID('PROJECT') IS NOT NULL
    DROP TABLE PROJECT;

IF OBJECT_ID('WORKER') IS NOT NULL
    DROP TABLE WORKER;

CREATE TABLE PROJECT (
    ProjCode        INT PRIMARY KEY,
    ProjectTitle    NVARCHAR(30)
)

CREATE TABLE WORKER (
    WID             INT,
    WName           NVARCHAR(30),
    Gender          NVARCHAR(1),
    ProjCode        INT,
    PRIMARY KEY (WID),
    FOREIGN KEY (ProjCode) REFERENCES PROJECT(ProjCode)
)

